    private void tblTablaPersonaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTablaPersonaMouseClicked
        fila = tblTablaPersona.getSelectedRow();
        if (fila >= 0) {
            pc.setPersona(modeloPersona.getPersonas()[fila]);
            txtNombre.setText(pc.getPersona().getNombre());
            txtFechaNac.setText(pc.getPersona().getFechaNac());
            

        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un dato de la tabla", "Error", JOptionPane.ERROR_MESSAGE);

        }
        btnEditar.setEnabled(true);
    }//GEN-LAST:event_tblTablaPersonaMouseClicked

